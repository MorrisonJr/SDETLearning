package week1G;

public class OperatorSigns {

    public static void main(String[] args) {

        // Operator Signs in JAVA

        // '"' -> Assignment Operator

        int number = 83921;


        //'==' -> Equal sign or Comparison Operator

        boolean isTrue = 5+2 == 9;

        System.out.println(isTrue);

        //'!=' -> Not Equal sign in JAVA

        boolean isFalse = 5+2 != 7;

        System.out.println(isFalse);

        //'>' -> Greater Than sign in JAVA

        isTrue = 12 > 13;

        System.out.println("Greater Than: " + isTrue);

        //'<' -> Less Than sign in JAVA

        isTrue = 12 < 13;

        System.out.println("Less Than: " + isTrue);

        //'>=' -> Greater Than or Equal To sign in JAVA

        isTrue = 12 >= 13;

        System.out.println("Greater or Equal: " + isTrue);

        //'<=' -> Less Than or Equal To sign in JAVA

        isTrue = 12 <= 13;

        System.out.println("Less Than or Equal: " + isTrue);
    }
}
